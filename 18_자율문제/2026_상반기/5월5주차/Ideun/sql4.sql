SELECT
    HISTORY_ID
    , DAILY_FEE * (C.END_DATE - C.START_DATE +1) * (100-NVL(D.DISCOUNT_RATE,0))/100 AS FEE
FROM
    (
    SELECT
        A.HISTORY_ID
        , A.CAR_ID
        , CASE WHEN END_DATE - START_DATE +1 >=90 THEN '90일 이상'
        WHEN END_DATE - START_DATE +1 >=30 THEN '30일 이상'
        WHEN END_DATE - START_DATE +1 >=7 THEN '7일 이상'
        ELSE NULL END AS DIFF_DATE
        , A.START_DATE
        , A.END_DATE
        , B.CAR_TYPE
        , B.DAILY_FEE
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY A
       JOIN CAR_RENTAL_COMPANY_CAR B
        ON A.CAR_ID = B.CAR_ID
) C
LEFT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN D
    ON C.CAR_TYPE = D.CAR_TYPE
    AND C.DIFF_DATE = D.DURATION_TYPE
WHERE C.CAR_TYPE = '트럭'
ORDER BY 2 DESC, 1 DESC

