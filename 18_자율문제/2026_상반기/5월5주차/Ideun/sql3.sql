SELECT
    MONTH,
    CAR_ID,
    COUNT(*) AS RECORDS
FROM
(
    SELECT
        EXTRACT(MONTH FROM START_DATE) AS MONTH
        , CAR_ID
        , COUNT(*) OVER(PARTITION BY CAR_ID) AS RECORDS
    FROM
        CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE BETWEEN DATE '2022-08-01' AND DATE '2022-10-31'
)
WHERE RECORDS >=5
GROUP BY
    MONTH
    , CAR_ID
ORDER BY 1, 2 DESC

