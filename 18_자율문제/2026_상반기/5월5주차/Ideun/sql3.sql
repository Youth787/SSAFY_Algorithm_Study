SELECT
   '/home/grep/src/'|| A.BOARD_ID||'/'||B.FILE_ID||B.FILE_NAME||B.FILE_EXT AS FILE_PATH
FROM
(
    SELECT
        BOARD_ID
    ,   VIEWS
    FROM USED_GOODS_BOARD
    ORDER BY VIEWS DESC
    FETCH FIRST 1 ROWS ONLY
) A
LEFT JOIN USED_GOODS_FILE B
ON A.BOARD_ID = B.BOARD_ID
ORDER BY 1 DESC



