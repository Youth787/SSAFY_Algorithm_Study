WITH ANS AS (
    SELECT 
        ROW_NUMBER() OVER(PARTITION BY S.USER_ID, S.PROBLEM_ID ORDER BY TIMESTAMP) AS NTH_SUBMISSION 
        , CASE WHEN S.SUBMITTED = P.CORRECT_ANSWER THEN 'Y' 
            ELSE 'N' END AS ANS
    FROM SUBMISSIONS S 
        JOIN PROBLEMS P 
        ON S. PROBLEM_ID = P.PROBLEM_ID
)

SELECT 
    NTH_SUBMISSION
    , ROUND(SUM(CASE WHEN ANS = 'Y' THEN 1 ELSE 0 END) * 100 / COUNT(*), 0) AS CORRECT_RATE 
FROM ANS 
GROUP BY NTH_SUBMISSION 
ORDER BY 1 
